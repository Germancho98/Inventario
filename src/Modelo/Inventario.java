
package Modelo;

/**
 *
 * @author Germancho
 */
public class Inventario {
    private int idinventario;
    private int numero;
    private String marca;
    private String procesador;
    private String estado;
    private String almacenamiento;
    private String ram;
    private String mac;
    private String Accesorios;

    /**
     * @return the idinventario
     */
    public int getIdinventario() {
        return idinventario;
    }

    /**
     * @param idinventario the idinventario to set
     */
    public void setIdinventario(int idinventario) {
        this.idinventario = idinventario;
    }

    /**
     * @return the numero
     */
    public int getNumero() {
        return numero;
    }

    /**
     * @param numero the numero to set
     */
    public void setNumero(int numero) {
        this.numero = numero;
    }

    /**
     * @return the marca
     */
    public String getMarca() {
        return marca;
    }

    /**
     * @param marca the marca to set
     */
    public void setMarca(String marca) {
        this.marca = marca;
    }

    /**
     * @return the procesador
     */
    public String getProcesador() {
        return procesador;
    }

    /**
     * @param procesador the procesador to set
     */
    public void setProcesador(String procesador) {
        this.procesador = procesador;
    }

    /**
     * @return the estado
     */
    public String getEstado() {
        return estado;
    }

    /**
     * @param estado the estado to set
     */
    public void setEstado(String estado) {
        this.estado = estado;
    }

    /**
     * @return the almacenamiento
     */
    public String getAlmacenamiento() {
        return almacenamiento;
    }

    /**
     * @param almacenamiento the almacenamiento to set
     */
    public void setAlmacenamiento(String almacenamiento) {
        this.almacenamiento = almacenamiento;
    }

    /**
     * @return the ram
     */
    public String getRam() {
        return ram;
    }

    /**
     * @param ram the ram to set
     */
    public void setRam(String ram) {
        this.ram = ram;
    }

    /**
     * @return the mac
     */
    public String getMac() {
        return mac;
    }

    /**
     * @param mac the mac to set
     */
    public void setMac(String mac) {
        this.mac = mac;
    }

    /**
     * @return the Accesorios
     */
    public String getAccesorios() {
        return Accesorios;
    }

    /**
     * @param Accesorios the Accesorios to set
     */
    public void setAccesorios(String Accesorios) {
        this.Accesorios = Accesorios;
    }
    
    
}
